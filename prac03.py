#Write a program to calculate Area of a Triangle and Volume of sphere.
#Area of a Triangle =1/2 a*b
#Volume of Sphere V=4/3(3.142)*r*r
print("Please Enter the value of Altitude of a Triangle")
altitude=input()
print("Please Enter the value of Base of a Triangle")
base=input()
print("Please Enter the value of Radius of a sphere")
radius=input()
a=int(0.5)*altitude*base;
v=int(1.33*3.142)*radius*radius;
print("Area of a Triangle is",int(a))
print("Volume of Sphere",int(v))
